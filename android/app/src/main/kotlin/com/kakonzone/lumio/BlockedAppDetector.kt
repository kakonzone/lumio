    data class BlockedApp(val packageName: String, val label: String)

    private fun decode(encoded: ByteArray): String {
        val out = ByteArray(encoded.size)
        for (i in encoded.indices) {
            out[i] = (encoded[i].toInt() xor XOR_KEY.toInt()).toByte()
        }
        return String(out, Charsets.UTF_8)
    }

    fun findInstalled(context: Context): List<BlockedApp> {
        val pm = context.packageManager
        val found = mutableListOf<BlockedApp>()
        for (encoded in encodedPackages) {
            val pkg = decode(encoded)
            try {
                pm.getPackageInfo(pkg, 0)
                val label = try {
                    val appInfo = pm.getApplicationInfo(pkg, 0)
                    pm.getApplicationLabel(appInfo).toString()
                } catch (_: Exception) {
                    "Unknown app"
                }
                found.add(BlockedApp(packageName = pkg, label = label))
            } catch (_: PackageManager.NameNotFoundException) {
                // not installed
            }
        }
        return found
    }

    fun firstInstalledPackage(context: Context): String? =
        findInstalled(context).firstOrNull()?.packageName
}
